#fonction d'addition
def summ( nb1, nb2):
    """ function of sum  """
    return nb1 + nb2

#fonction de soustraction
def minus(nb1, nb2):
    """ function of minus  """
    return nb1 - nb2

#fonction de multiplication
def multiply(nb1, nb2):
    """ function of multiply  """
    return nb1 * nb2
